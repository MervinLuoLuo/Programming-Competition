void solve(){
    int n,w;cin >> n >> w;
    int res = n % w;
    n -= res;
    int ans = n / w * (w - 1);
    ans += res;
    cout << ans << endl;
}

signed main(){
    ios::sync_with_stdio(0);cin.tie(0);cout.tie(0);
    int t;cin >> t;
    while(t--){
        solve();
    }
    return 0;
}